// Translation function
function t(key, lang = 'en-IN') {
    return translations[lang]?.[key] || translations['en-IN'][key] || key;
}

// Get current language from localStorage or user preference
function getCurrentLanguage() {
    return localStorage.getItem('shebalance_language') || 'hi-IN';
}

// Set language
function setLanguage(lang) {
    localStorage.setItem('shebalance_language', lang);
    updateUILanguage(lang);
}

// Update all UI elements with translations
function updateUILanguage(lang) {
    console.log('🌍 Updating UI language to:', lang);
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = t(key, lang);
        if (translation !== key) {
            element.textContent = translation;
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = t(key, lang);
    });
    
    // Update specific text content by class or ID
    updateDynamicContent(lang);
    
    // Trigger custom event for dynamic content
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    
    console.log('✅ UI language updated');
}

